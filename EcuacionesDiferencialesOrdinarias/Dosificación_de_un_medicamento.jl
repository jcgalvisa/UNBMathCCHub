# ╔═╡ 30310b6e-99e7-4aae-88cf-828668e3803d
md"""Para desarrollar este modelo, tendremos en cuenta que el medicamento que se ingiere por vía oral debe pasar inicialmente por el tracto digestivo hasta el momento en que, gracias al proceso de digestión, pueda ser procesado e incorporado a la sangre. En el caso específico del paracetamol, se espera que inhiba la producción de prostaglandinas tanto en el cerebro como en todo el cuerpo.

Por lo tanto, para plantear el modelo, consideramos la función $D(t)$, que nos indica la concentración del medicamento en el tracto digestivo, y la función $S(t)$, que nos indica la concentración plasmática del fármaco.

Consideremos las siguientes variables:

- $C(t):= \text{Concentración del fármaco en el cuerpo de un individuo en el tiempo }t.$

- $f= \text{Cantidad de fármaco suministrada al individuo.}$

- $v_d= \text{Volumen de distribución.}$

- $c_l= \text{aclaramiento del medicamento.}$

- $t= \text{Tiempo.}$

- $\alpha= \text{Tasa de eliminación del medicamento del tracto digestivo.}$

- $\beta = \text{Tasa de eliminación del medicamento de la sangre.}$"""

# ╔═╡ 558b526d-69dd-4fc7-a596-b974d7885c64
md""" Sea $\beta$ la constante de absorción del medicamento; esta constante de absorción es, por definición, la probabilidad de que una molécula se absorba en la unidad de tiempo. Sea $\alpha$ la constante de eliminación del medicamento. Podemos afirmar que nuestro sistema se comporta de la siguiente manera:"""

# ╔═╡ 5b813fdb-c5f4-48c6-b59e-8598e4a6ff5f
begin
	url2 = "https://github.com/laboratoriodealgebralineal/laboratoriodealgebralineal.github.io/blob/main/Im%C3%A1genes/acetaminofen2.png?raw=true"
