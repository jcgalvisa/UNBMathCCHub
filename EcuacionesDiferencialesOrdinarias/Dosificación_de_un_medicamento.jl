md"""# Ejemplo

Consideremos tabletas de 500 mg de Dolex Tabletas (Acetaminofen o Paracetamol). Se sabe que la vida media de este fármaco es de 1 a 4 horas, y que las dosis recomendadas son

- Adultos: 1-2 tabletas cada 4-6 horas.
- Niños 6-12 años: $\frac{1}{2}-1$ tableta cada 4-6 horas.
- Dosis máxima diaria: Es de 8 tabletas (4000 mg) cada 24 horas. El intervalo mínimo de administración es de 4 horas. Cuando sea necesario repetir la dosis cada 4 horas, debe administrarse 1 tableta."""

# ╔═╡ 1367bf0d-5213-4607-98e3-87f979208c16
md"""Se tomará como ejemplo una persona cuyo peso es de 60 kg, y cuyo organismo es tal que la vida media del medicamento es de 3 horas. Se supondrá que toma una tableta cada 6 horas (es decir, consume la dosis mínima recomendada). Así $f=\frac{500mg}{60kg}=8.334mg/kg.$


En este modelo se tienen dos parámetros a determinar, el primero es la constante de eliminación $\alpha$ y la segunda constante, es la constante de absorción $\beta$, está representa la velocidad con la que el fármaco es absorbido por el torrente sanguíneo. Para hallar estas constantes usaremos el tiempo máximo.

$t_{max}=\frac{\ln{\left(\frac{\alpha}{\beta}\right)}}{\alpha-\beta}$
En donde $t_{max}$ es el tiempo en donde se alcanza la concentración máxima, este tiempo nos es dado en la información sobre el acetaminofén, en donde dice que es de 30-60 minutos.

Entonces, suponiendo una persona de peso promedio, se va a tomar el promedio del valor máximo en tiempo como $t_{max}=45$ minutos que equivale a $0.75$ horas, entonces reemplazando:
 
$0.75=\frac{\ln{\left(\frac{\alpha}{\beta}\right)}}{\alpha-\beta}.$

Además, sabemos que la vida media del farmaco ($t_{1/2}$) satiface la siguiente igualdad

$\frac{\beta}{\alpha-\beta}\left(e^{-\beta t_{1/2}}-e^{-\alpha t_{1/2}}\right)+e^{-\beta t_{1/2}}=\dfrac{1}{2}.$

Resolvamos dicho sistema de ecuaciones para hallar los valores de los parámetros:
"""

# ╔═╡ fafb45f2-1a55-4cea-abe6-c24d3c9e75c3
begin
	# Definir las constantes
	tm = 3  # Vida media
	tmax = 45/60 # Tiempo donde se alcanza el máximo

	# Definir la función del sistema
	function system2!(F, x)
    	a, b = x[1], x[2]
    	F[1] = log(a / b) / (a - b) - tmax
    	F[2] = ((b/(a-b))*(exp(-b*tm)-exp(-a*tm))+exp(-b*tm)) - (1/2)
	end

	initial_guess = [0.1, 1] # paso inicial

	result = nlsolve(system2!, initial_guess) # Calcular la solución usando nlsolve

	println("Resultado: ", result.zero)
end

# ╔═╡ 95948b3d-8e89-4152-b383-8991edfbd767
md"""De esto se obtiene que $\alpha = 0.2535$ y $\beta= 3.8972$"""

# ╔═╡ 1ec04274-801b-49df-9914-d4126f6720ce
md"""Para la aplicación mínima efectiva $CME$ se tomarán los mismos datos que en el modelo anterior, es decir $T=6$ y $f=\frac{500mg}{60kg}=8.334\text{ mg/kg }$, teniendo en cuenta las constantes del modelo calculadas dadas por $\alpha=0.2535$ (1/h) y $\beta=3.8972$ (1/h) entonces la concentración mínima efectiva del acetaminofén está dada por:  

$CME=8.334\text{ mg/kg }\frac{r}{1-r}$
en donde  $r=\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}}\right).$ 

Es decir, para los valores anteriormente dichos se tiene que
$r=$
"""

# ╔═╡ 873acad0-28b0-46d8-94b6-d0e9c6d374de
begin
	α₁, β₁ =  result.zero
	T = 6
	r = (β₁/(α₁-β₁))*(exp(-β₁*T)-exp(-α₁*T))+exp(-β₁*T)
end

# ╔═╡ 2cc2b82f-2c4c-414b-bb14-341cd8d08838
md"""De aquí, se concluye que la concentración mínima efectiva es de:"""

# ╔═╡ 6a9fee11-3f1c-4668-8468-f381ea99d798
@bind peso Slider(30:100, show_value=true, default=60)

# ╔═╡ 6e932266-a899-4b76-bc36-2ac70f671132
(500/peso)*(r/(1-r)) #mg/kg

# ╔═╡ c7c63755-9680-4b2f-a4af-cfd2bb44f5d1
md"""Ahora para la concentración máxima tolerada se toman los mismos datos, sabiendo que $r=0.2337$ se sigue que la $CMT$ está dada por"""

# ╔═╡ 0a2a335b-616f-47ca-a4a0-79c3c389e5f4
(500/peso)*(1/(1-r)) #mg/kg

# ╔═╡ 743659cf-e32f-4a3e-b6d3-067ac6585dbf
md"""Visualicemos la concentración del medicamento en el cuerpo a lo largo de 48 horas."""

# ╔═╡ 4e51f432-be05-4f97-9ca3-c876f3912e73
let
	beta = β₁
	alpha = α₁
	# Definir parámetros
	T = 6
	tm = 3
	tmax = 45 / 60
	f = 500 / peso
	n = 6

	CMT(t) = (500/peso)*(1/(1-r))
	CME(t) = (500/peso)*(r/(1-r))

	C1 = f*(beta/(alpha-beta))*(exp(-beta*T)-exp(-alpha*T))+exp(-beta*T)

	# Crear rango de tiempo
	Tiempo = LinRange(0, 48, 1000)
		
	# Definir función b
	function b(t, T, n)
    	i = ceil(t / T)
    	if t / T <= n
			
        	C = (f/(alpha-beta)) * (-beta*exp(-alpha * t) * ((1 - exp(i * alpha * T)) / (1 - exp(alpha * T))) + alpha*exp(-beta * t) * ((1 - exp(i * beta * T)) / (1 - exp(beta * T))))
    	else
        	C = (f/(alpha-beta)) * (-beta*exp(-alpha * t) * ((1 - exp(n * alpha * T)) / (1 - exp(alpha * T))) + alpha*exp(-beta * t) * ((1 - exp(n * beta * T)) / (1 - exp(beta * T))))
    	end
    	return C
	end

	# Graficar
	plot(Tiempo, CMT.(Tiempo))
	plot!(Tiempo, CME.(Tiempo))
	
	plot!(Tiempo, [b(k, T, n) for k in Tiempo], color="black", label="Concentración")
	
	scatter!([i * T for i in 1:n], [b(t, T, n) for t in [i * T for i in 1:n]], color="blue", label="C_min")
	
	scatter!([0.11 + i * T for i in 0:n-1], [b(t, T, n) for t in [0.1 + i * T for i in 0:n-1]], color="red", label="C_max")
	
	title!("Concentración del medicamento (500mg cada 6h)")
	xlabel!("Tiempo (h)")
	ylabel!("Concentración (mg/kg)")
	xlims!(0, 48)
	ylims!(0, 15)
end

