# ╔═╡ e936780e-66e4-4266-81d1-9ca33553cf98
md"""Así, la concentración del fármaco que pierde el tracto digestivo es a través del tiempo es $-\beta D$, es decir

$\dfrac{dD}{dt}=-\beta D.$
Y la concentración del fármaco en la sangre está dada por lo que gana proveniente del tracto digestivo, menos lo que elimina, lo cual, se expresa de la siguiente manera:

$\dfrac{dS}{dt}=\beta D-\alpha S.$
Estableciendo las condiciones iniciales para $D(t)$ y para $S(t)$  como $D(0)=f$ donde $f$ es la dosis de medicamento suministrada y $S(0)=0$ puesto que inicialmente en la sangre no hay presencia de medicamento, se sigue el siguiente problema

$\dfrac{dD}{dt}=-\beta D \hspace{0.2cm}\text{ donde } \hspace{0.2cm}D(0)=f,$
$\dfrac{dS}{dt}=\beta D-\alpha S \hspace{0.2cm}\text{ donde }\hspace{0.2cm}S(0)=0.$"""

# ╔═╡ 8a0a1400-1500-4912-b739-486059c0648b
md"""Resolvamos el sistema. Las variables simbólicas que se usarán son las siguientes:"""

# ╔═╡ 780d0ffa-0ce7-4e29-beb5-cccb06e42e7e
@syms D() S() t f α β

# ╔═╡ 73c51869-2cbb-4e3b-8a41-042516195ec5
md"""Resolvamos la primera ecuación. Iniciemos definiendo la ecuación diferencial."""

# ╔═╡ 5b275120-ec92-47af-a79c-33e2d66d12d5
begin
	d = Differential(t)
	eqn = d(D)(t) ~ -β*D(t) #D' = -βD
end

# ╔═╡ f78b08fb-ac28-4a14-a5bd-695f98c1c312
md"""Luego la resolvemos de la siguiente manera"""

# ╔═╡ 0196f060-39a2-46ca-a3c3-66c010fefaec
solucion = dsolve(eqn)

# ╔═╡ e20f1893-2732-4c9f-92d7-0fdf3cffb347
md"""Luego, resolvemos el problema de valor inicial, $D(0)=f$. Así $C_1=$"""

# ╔═╡ 8c41a81a-8f58-411b-869d-39feb0915210
begin
	eq = rhs(solucion)   
	C1 = first(setdiff(free_symbols(eq), (t,β))) 
	c1 = solve(eq(t=>0) - f, C1)[1] #D(0)=f
end

# ╔═╡ 6850c2cf-ce02-443f-a611-044aa307de2c
md"""Por tanto, $D(t)=fe^{-t\beta}$."""

# ╔═╡ 97fd3e50-6b04-40f9-acfb-5c5914251190
D₁(t)=eq(C1 => c1)

# ╔═╡ 12d5fa4e-1b09-4a0a-9e9f-4c7d65ac1647
md"""Resolvamos la segunda ecuación diferencial. Iniciemos definiendo la ecuación:"""

# ╔═╡ 103d7e0f-4373-4230-abf6-e751d1dcb6a9
eqn2 = d(S)(t) ~ β*D₁(t)-α*S(t) #S' = βD-αS

# ╔═╡ 5ef0f706-8a98-42e8-8ce5-45f66b861ebc
md"""Luego la resolvemos de la siguiente manera"""

# ╔═╡ 348bd8f0-bcdc-4a6f-8687-d500af352ce4
solucion2 = dsolve(eqn2)

# ╔═╡ 0b00b02f-6bbc-486b-aba7-6a1b2f0daf57
md"""Luego, resolvemos el problema de valor inicial, $S(0)=0$. Así $C_1=$"""

# ╔═╡ 4721f373-d455-4ed0-a2a9-d5fb721a3d17
begin
	eq2 = rhs(solucion2)   
	C1₂ = first(setdiff(free_symbols(eq2), (f,t,α,β))) 
	c1₂ = solve(eq2(t=>0) - 0, C1₂)[1] #S(0)=0
end

# ╔═╡ be95b55e-d447-4d2c-86b4-c1ed915ff08b
S₁(t)=eq2(C1₂ => c1₂)

# ╔═╡ 29945112-73f9-430e-947f-95da76ed3be4
md"""Así $S(t)=\dfrac{f\beta e^{-t\beta}}{\alpha-\beta}$.
Así se obtiene que la solución del sistema de ecuaciones diferenciales correspondientes al modelo planteado para determinar la concentración de un medicamento en un tiempo $t$ es la siguiente:

$\left \{
    \begin{aligned}
    D(t)&=fe^{-\beta t}, \\
      S(t)&=\frac{\beta f}{e^{\beta t}(\alpha-\beta)}-\frac{\beta f}{e^{\alpha t}(\alpha-\beta)}.
    \end{aligned}
   \right .$

Ahora, teniendo en cuenta que nos interesa saber la cantidad de concentración de un medicamento en el cuerpo humano, se tiene que 

$C(t)=D(t)+S(t)=fe^{-\beta t}+\frac{\beta f}{\alpha-\beta}\left(e^{-\beta t}-e^{-\alpha t}\right).$

Esto contemplando un comportamiento en el cual el medicamento pasa por distintos procesos para ser absorbido."""

