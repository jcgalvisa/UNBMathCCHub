# ╔═╡ 14863713-bd03-45ff-a996-bae997820b8e
md"""## Vida media del fármaco

La duración media de acción de un fármaco se establece como el periodo en el cual su concentración se reduce a la mitad. Para calcular el tiempo que el organismo emplea en eliminar la mitad del medicamento administrado, se iguala $C(t)$ con la cantidad correspondiente a la mitad del fármaco presente en el cuerpo, es decir, $\dfrac{f}{2}$. Esto conduce a la siguiente expresión

$\frac{\beta f}{e^{\beta t}(\alpha-\beta)}-\frac{\beta f}{e^{\alpha t}(\alpha-\beta)}+fe^{-\beta t}=\dfrac{f}{2},$
esto es 
        
$\frac{\beta}{\alpha-\beta}\left(e^{-\beta t}-e^{-\alpha t}\right)+e^{-\beta t}=\dfrac{1}{2}.$
        
Luego, $t_{1/2}$ es la solución de la anterior ecuación lo lineal."""

# ╔═╡ 4aeaa87c-1390-448b-868e-746a7058bc80
md"""## Concentración después de $n$ tomas

Para obtener la concentración después de $n$ donde $T$ es el tiempo entre cada una de ellas. Vamos a asumir que inicialmente la persona toma una pastilla. Según el modelo proporcionado para la concentración de medicamento en el tiempo $t$, se determina que en el periodo $t = T$, la concentración de dicho medicamento es

$C_1(T)=\dfrac{f\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{f}{e^{\beta T}}=f\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}}\right),$
donde recordemos que $f$ es la cantidad ingerida oralmente, definamos 

$r=\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}},$ 
luego $C_1(T)=f\cdot r$, despejando $r$ se tiene que $r=\dfrac{C_1(T)}{f}$. Luego de transcurrir otro periodo de tiempo $T$, se consume otra pastilla, luego la concentración de este fármaco en el cuerpo es

$C_2(T)=(f+C_1(T))r=(f+C_1(T))\dfrac{C_1(T)}{f}=\dfrac{C_1^2(T)}{f}+C_1(T).$
A partir de ahora, la concentración de medicamento después de $n$ tomas en un periodo $T$ se representará como $C_n(T)$. Por lo tanto, para cualquier cantidad $n$ de tomas separadas por $T$ unidades de tiempo, se observa que

$\left \{
    \begin{aligned}
      C_n(T) &= (C_{n-1}(T)+f)r,\\
      C_0 &= f.
    \end{aligned}
  \right .$
esto es

$\left \{
    \begin{aligned}
      C_n(T) &= (C_{n-1}(T)+f)\dfrac{C_1(T)}{f},\\
      C_0 &= f.
    \end{aligned}
  \right .$
Resolviendo esta recursión se tiene que

$\left \{
    \begin{aligned}
      C_{n}(T) &= \sum_{j=1}^{n}\dfrac{C_1^k(T)}{f^{k-1}},\\
      C(0) &= f.
    \end{aligned}
  \right .$
Usando la ecuación anterior se tiene que al consumir $n\in\mathbb{N}$ pastillas entonces la concentración del fármaco en el cuerpo es 

$\begin{align*}
    C(T)&=\sum_{j=1}^{n}\dfrac{C_1^k(T)}{f^{k-1}}\\
    &= f \sum_{j=1}^{n}\left(\dfrac{C_1(T)}{f}\right)^k
\end{align*}$
Teniendo en cuenta que esta es una suma geométrica finita de razón $\frac{C_1(T)}{f}$, por ende

$\begin{align*}
    C_n(T)&=f\left(\dfrac{C_1(T)}{f}\cdot\dfrac{1-\left(\dfrac{C_1(T)}{f}\right)^n}{1-\dfrac{C_1(T)}{f}}\right)\\
    &=C_1(T)\left(\dfrac{f^n-C_1^n(T)}{(f-C_1(T))f^{n-1}}\right)
\end{align*}$
donde recordemos que $C_1(T)= f\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}}\right)$."""

# ╔═╡ 644d44c0-5f52-483e-bf9b-60517eed723e
md"""## Concentración para un número grande de tomas

Ahora, deseamos saber cuál es la concentración para un número grande de tomas (enfermedad crónica). Anteriormente, se demostró que la concentración después de $n$ tomas espaciadas en un intervalo $T$ de tiempo especificado es

$C_n(T)=f \sum_{j=1}^{n}\left(\dfrac{C_1(T)}{f}\right)^k,$
luego, cuando la persona llega a un número suficientemente grande ($n\to\infty$) de tomas del fármaco, se sigue que

$C_{\infty}=f \sum_{j=1}^{\infty}\left(\dfrac{C_1(T)}{f}\right)^k,$
observe que $C_1(T)<f$ para $T>0$, por tanto $\frac{C_1(T)}{f}<1$, así la serie anterior al ser una serie geométrica con razón menor que 1, se tiene que ella converge. Así la concentración para un numero grande de tomas espaciadas por un periodo $T$ de tiempo cada una de estas es de

$\begin{align*}
            C_{\infty}&=f \sum_{j=1}^{\infty}\left(\dfrac{C_1(T)}{f}\right)^k\\
            &=f\left(\sum_{j=0}^{\infty}\left(\dfrac{C_1(T)}{f}\right)^j-1\right)\\
            &= f\left(\dfrac{1}{1-\frac{C_1(T)}{f}}-1\right)\\
            &=f\left(\dfrac{C_1(T)}{f-C_1(T)}\right)\\
            &=\dfrac{C_1(T)f}{f-C_1(T)}.
        \end{align*}$"""

# ╔═╡ bc045e77-f12e-45cc-a2b0-629f3657154d
md"""## Concentración para intervalos cortos

La concentración en períodos breves entre dosis para pequeñas cantidades de tomas es análoga a examinar la concentración para cantidades mayores de tomas. Esto se debe a que, cuando el tiempo se acerca a cero, implica que se están tomando muchas pastillas en un lapso corto.

Por lo tanto, al analizar la concentración en períodos cortos entre dosis, en los cuales se han ingerido un número considerable de tomas, recordemos que en la sección $c)$ se estableció la expresión para la concentración cuando se realizan un gran número de tomas:

$C_{\infty}(T)=\dfrac{C_1(T)f}{f-C_1(T)},$
donde $C_1(T)= f\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}}\right)$, luego para $T\to 0$ se tiene que 

$\begin{align*}
            \lim_{T\to 0} \dfrac{C_1(T)f}{f-C_1(T)} &= \lim_{T\to 0} \dfrac{\left(f\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}}\right) \right)f}{f-\left( f\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}}\right)\right)} \\
            &=\dfrac{\left(f\left(\dfrac{\beta}{\alpha-\beta}\left(1-1\right)+1\right) \right)f}{f-\left( f\left(\dfrac{\beta}{\alpha-\beta}\left(1-1\right)+1\right)\right)} \\
            &=\dfrac{f^2}{0}\\
            &= \infty
        \end{align*}$
        
Esta observación nos indica que al consumir múltiples pastillas con breves lapsos de tiempo entre cada una, la concentración en el cuerpo tiende a aumentar sin límite. Esto sugiere que a medida que disminuimos el tiempo entre las dosis, la concentración del fármaco se incrementa progresivamente, alcanzando teóricamente un valor infinito cuando el tiempo entre dosis tiende a cero."""

# ╔═╡ 2af79425-51d2-4b9a-bd4a-80a48e3e7c1d
md"""## CME para fines terapéuticos

Ahora, hallemos la concentración mínima efectiva (CME) para fines terapéuticos. Dado que el fármaco se consume cada $T$ unidades de tiempo en una dosis de $f$ miligramos, durante ese intervalo, la concentración del fármaco exhibe un comportamiento que crece muy rápidamente y después de un tiempo sigue con una disminución exponencial desde $C_0$ hasta alcanzar el valor mínimo $m_1$, esto en el intervalo de tiempo $[0, T]$, así 

$m_1=fr,$
donde $r=\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta T}}-\dfrac{1}{e^{\alpha T}}\right)+\dfrac{1}{e^{\beta T}}\right).$

Ingerir nuevamente la misma cantidad de medicamento una vez transcurridas las $T$ unidades de tiempo implica que la concentración aumenta hasta un valor de  $m_1+f$. Esta cifra representa la concentración máxima en el intervalo de tiempo $[T, 2T]$. Al finalizar este intervalo, la concentración será de

$m_2=(m_1+f)r=(fr+f)r=fr^2+fr.$
Continuando con este análisis es posible ver que 

$\begin{align*}
    m_1&=fr\\
    m_2&=fr^2+fr\\
    m_3&=fr^3+fr^2+fr\\
    \vdots
\end{align*}$
Luego, los puntos mínimos se van acercando a la concentración mínima de equilibrio, lo cual puede ser visualizado como una serie convergente:

$\begin{align*}
    m&=fr+fr^2+fr^3+fr^4+\cdots\\
    &=\sum_{k=1}^{\infty} fr^k\\
    &=f\left(\sum_{k=0}^{\infty}r^k-1\right)
\end{align*}$
Esta es una serie geométrica con razón menor a 1, ya que si definimos $g(x)=\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta x}}-\dfrac{1}{e^{\alpha x}}\right)+\dfrac{1}{e^{\beta x}}$, se observa que $g(0)=1$ y además la función es decreciente, ya que

$\begin{align*}
    g'(x)&=\frac{d}{dx}\left(\dfrac{\beta}{\alpha-\beta}\left(\dfrac{1}{e^{\beta x}}-\dfrac{1}{e^{\alpha x}}\right)+\dfrac{1}{e^{\beta x}}\right)\\
    &=\dfrac{\beta}{\alpha-\beta}\left(-\beta e^{-\beta x}+\alpha e^{-\alpha x}\right)-\beta e^{-\beta x}\\
    &= \dfrac{\alpha\beta}{\alpha-\beta}(e^{-\alpha x}-e^{-\beta x})
\end{align*}$
Ahora, si $\alpha>\beta$ se tiene que $e^{-\alpha x}-e^{-\beta x}<0$, luego $g'(x)<0$. Y si $\beta > \alpha$ se tiene que $\alpha-\beta <0$, así $g'(x)<0$. Como $g'(x)<0$ para todo $x\in (0, \infty)$, entonces $g(x)$ es decreciente para  todo $x\in (0, \infty)$, y como se menciono que para $x=0$ se tiene que $g(0)=1$, entonces $g(x)<1$ para todo $x\in (0, \infty)$.
Al evaluar la función en $x=T$ se tiene que $g(T)=r$, y por transitividad se tiene que $r<1$.

Luego $m=f\left(\dfrac{1}{1-r}-1\right)=\dfrac{fr}{1-r}.$

Esta expresión nos indica que a medida que se agregan términos infinitos a la serie, los valores mínimos de concentración, que están representados por la secuencia m, se acercan gradualmente hacia la concentración de equilibrio."""

# ╔═╡ a22018e8-e2ef-4d30-84dd-a8c4bf24fd43
md"""## Concentración máxima permitida

Calculemos ahora la concentración máxima permitida o concentración mínima tóxica (CMT). Considerando que la administración del medicamento ocurre en intervalos de tiempo de longitud $T$, asumiendo que la concentración inicial del fármaco en el organismo es $f$, y considerando que la concentración plasmática disminuye exponencialmente desde $C_0$ hasta el valor mínimo $m_1$, la concentración en el momento en que se va a ingerir la segunda dosis es:

$M_2=m_1+f,$
tomando nuevamente la misma dosis (esta ya sería la tercera dosis) del medicamento luego de un tiempo $T$ se tiene que la concentración máxima en el cuerpo es

$M_3=m_2+f,$
siguiendo este proceso se observa que

$\begin{align*}
            M_2&=fr+f\\
            M_3&=fr^2+fr+f\\
            M_4&=fr^3+fr^2+fr+f\\
            &\vdots
        \end{align*}$
De esta manera, los picos máximos se pueden ver como una serie convergente

$M=f+fr+fr^2+fr^3+\cdots,$
anteriormente mostramos que $r<1$, luego la siguiente serie geométrica es convergente
        
$f\sum_{k=0}^{\infty}r^k=\dfrac{f}{1-r}.$
siendo esta la máxima concentración permitida."""

